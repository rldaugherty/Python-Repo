#VARIABLES: You can think of variables as little tiny containers that can hold information.

# We've defined the variable "meal" here to the name of the food we ate for breakfast!
meal = "An english muffin" #Here, the "meal' variable is defined, and printed to the user using the "print()" function below.

# Printing out breakfast
print("Breakfast:")
print(meal) 

# Now update meal to be lunch!
meal = "A Sandwich" #Although defined above, here we overwrite the "meal' variable value to "A Sandwich", and print it to the user using the "print()" function below.

# Printing out lunch
print("Lunch:")
print(meal)

# Now update "meal" to be dinner
meal = "A Steak" # Here we redefine the variable again simply to show that it can be done as many times as necessary
# Printing out dinner
print("Dinner:")
print(meal)